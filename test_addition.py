# Предположим, что студенты должны написать функцию add(a, b), которая возвращает сумму двух чисел.

from student_code import add  # Импортируем функцию из файла, который напишут студенты.

def test_add_positive_numbers():
    assert add(2, 3) == 5, "Ошибка: функция должна вернуть 5 при сложении 2 и 3"

def test_add_negative_numbers():
    assert add(-1, -1) == -2, "Ошибка: функция должна вернуть -2 при сложении -1 и -1"

def test_add_zero():
    assert add(0, 0) == 0, "Ошибка: функция должна вернуть 0 при сложении двух нулей"
